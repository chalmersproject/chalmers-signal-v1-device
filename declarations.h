// Declaration for an SSD1306 display connected to I2C (SDA, SCL pins)
#define SCREEN_WIDTH 128 // OLED display width, in pixels
#define SCREEN_HEIGHT 64 // OLED display height, in pixels
#define Pot A0
//These pins are defined in the Adafruit SSD1306 library.
//Some displays have different names for pins, so I've included the 2 I've encountered
//GND --------> GND
//VCC --------> 5v
//D0 (CLK)----------> A5 (arduino) | D1 on NodeMCU
//D1 (MOSI)-----------> A4 | D2 on NodeMCU
//RES ---------> pin4 | LED_BUILTIN on NodeMCU (or D4)
#define OLED_RESET  LED_BUILTIN // Reset pin # (or -1 if sharing Arduino reset pin)

Adafruit_SSD1306 display(SCREEN_WIDTH, SCREEN_HEIGHT, &Wire, OLED_RESET);

